#include<iostream>

using namespace std;

int main() {

	int x; // variable declaration

	x = 10; // variable assignment

	cout << "x = " << x << endl;

	int y; 

	y = 10+20;

	cout << "y = " << y << endl;

	string z;

	z = "10+20";

	cout << "z = " << z << endl;

	string s1 = "coding";
	string s2 = "blocks";

	cout << "s1+s2 = " << s1+s2 << endl;

	cout << "s1 = " << s1 << endl;

	cout << "s2 = " << s2 << endl;

	return 0;
}